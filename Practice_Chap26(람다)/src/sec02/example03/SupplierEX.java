package sec02.example03;

import java.util.function.IntSupplier;
import java.util.function.Supplier;

public class SupplierEX {

	public static void main(String[] args) {
		
//		Supplier = 공급자 
		
		//Supplier<T>함수적 인터페이스는 매개값이 없고, T타입의 값을 리턴을 해준다. (공급자)
		//하여, 아래와 같이 함수적 인터페이스의 추상메서드인 get()을 람다식으로 제공을 해줬으며,
		//또한 호출도 한 결과이다.
		Supplier<String> supplier = () -> {
			String str = "자바 공부를 합니다.";
			return str;
		}; 
		String str = supplier.get();
		System.out.println(str);
		
		
		//위에 코드를 줄인것,,, return은 생략이 가능하며, return를 생략하면 {};도 생략이 가능하다.
		supplier = () -> "자바 공부를 합니다.";
		str = supplier.get();
		System.out.println(str);
		
		
		
		
		//IntSupplier는 매개값이 없으며,,, 리턴값이 Int이므로 무조건 Int로 받아야한다.
		//getAsInt()를 람다식으로 구현해서 호출해본 결과이다.
		//즉, getAsInt()가  ------ { () -> {int rand = (int)(Math.random()*45)+1;return rand;};   이것을 구현한것이다.
		IntSupplier intSupplier = () -> {
			int rand = (int)(Math.random()*45)+1;
			return rand;
		};
		int rand = intSupplier.getAsInt();
		System.out.println("IntSupplier함수적 인터페이스를 이용한 로또 번호 : "+rand);
		
		
		
		
		
		
		
		
		
	}

}
